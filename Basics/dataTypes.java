package Basics;

class dataTypes {
    public static void main(String[] args) {
        // byte range -127 to 128
        byte num1 = 100;
        // long range 
        long num2 = 1000;
        // 
        char ch = 'a';
        // double 
        double num3 = -0.001;
        // boolean
        boolean b = true;

        System.out.println("Number of byte: " + num1);
        System.out.println("Number of long: " + num2);
        System.out.println("Character used: " + ch);
        System.out.println("Double used: " + num3);
        System.out.println("Boolean used: " + b);
    }

}